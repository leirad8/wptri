  <h2 class="row">About the Event</h2>
  <div class="row">
    <div class="col-lg-6">
      <div class="box">
        <p>TIP Summit 2017 is the place to collaborate with Telecom Infra Project members and continue to develop new approaches to transforming telecom networks.</p>
        <p>See the latest innovations on the expo floor. Hear thought-provoking keynotes and insightful technical presentations from BT, Deutsche Telekom, Facebook, Reliance Jio and Telefonica. Network with new industry colleagues. These are just some of the highlights of attending TIP Summit 2017.</p>
      </div>
    </div>
    <div class="col-lg-6">
      <div class="box">
        <p>TIP members will also get progress updates from the OpenCellular, Open Optical Packet Transport, People and Process, and other TIP project groups.</p>
        <p>TIP Summit is open to members and non-members alike.</p>
